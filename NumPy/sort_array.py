#sorting array...
import numpy as np

#sort array int type...
arr = np.array([3, 2, 0, 1])
print(np.sort(arr))

#sort array int type...
arr = np.array(['banana', 'cherry', 'apple'])
print(np.sort(arr))

#sort array bool type...
arr = np.array([True, False, True])
print(np.sort(arr))


#sort 2-D array
arr = np.array([[3, 2, 4], [5, 0, 1]])
print(np.sort(arr))
